import '@/services/register-server';

import type { Metadata } from 'next';

import { Geist, Geist_Mono } from 'next/font/google';
import Script from 'next/script';
import { NextIntlClientProvider } from 'next-intl';
import { getLocale } from 'next-intl/server';

import { auth } from '@/auth';
import { GlobalMonitor } from '@/components/monitor';
import { Toaster } from '@/components/ui/sonner';

import { getThemeColor } from './server/theme-color';

import './globals.css';

const geistSans = Geist({
  variable: '--font-geist-sans',
  subsets: ['latin'],
});

const geistMono = Geist_Mono({
  variable: '--font-geist-mono',
  subsets: ['latin'],
});

export const metadata: Metadata = {
  title: 'Todo Demo For Next.js',
  description: 'Generated by create next app',
};

export default async function RootLayout({ children }: Readonly<{ children: React.ReactNode }>) {
  const [locale, color, session] = await Promise.all([getLocale(), getThemeColor(), auth()]);

  return (
    <html lang={locale} className={`theme-${color}`} suppressHydrationWarning>
      <head>
        <Script type="text/javascript" src="/theme.js" strategy="beforeInteractive" />
      </head>
      <body className={`${geistSans.variable} ${geistMono.variable} antialiased`}>
        <NextIntlClientProvider>
          <GlobalMonitor channelId={session?.user?.id ?? ''} />
          {children}
        </NextIntlClientProvider>
        <Toaster />
      </body>
    </html>
  );
}
